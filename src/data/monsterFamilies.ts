import type { MonsterFamily } from "../types/monster";

type FamilySeed = Omit<MonsterFamily, "characterWorld" | "baseElement" | "name"> & {
  baseElement?: never;
};

const family = (seed: FamilySeed): MonsterFamily => ({
  ...seed,
  name: `${seed.baseAnimalName}種`,
  characterWorld: "animal",
  baseElement: seed.defaultElement
});

type AdditionalFamilySeed = Pick<
  FamilySeed,
  "id" | "no" | "displayName" | "baseAnimalName" | "category" | "imageKey" | "emoji" | "defaultElement" | "signatureMark" | "biologicalMemo"
> &
  Partial<Pick<FamilySeed, "subElement" | "hiddenRareIds" | "silhouetteKey" | "baseBodyType" | "rarityBias" | "gameTrait">>;

const additionalFamily = ({
  hiddenRareIds = [],
  silhouetteKey = "current-asset",
  baseBodyType = "standard",
  rarityBias = 1,
  gameTrait,
  ...seed
}: AdditionalFamilySeed): MonsterFamily =>
  family({
    ...seed,
    personalityPool: ["おだやか", "こうきしん旺盛", "マイペース"],
    silhouetteTraits: [`${seed.baseAnimalName}らしいシルエット`, "丸みのある表情", "親しみやすい体つき"],
    fixedDesignTraits: [seed.signatureMark, "やわらかな表情"],
    variableDesignTraits: ["体色", "模様", "小さな飾り"],
    gameTrait: gameTrait ?? `${seed.baseAnimalName}らしい観察ポイントで発見の幅を広げます。`,
    researchHints: [
      `${seed.baseAnimalName}種は地域や時間帯で個体差が変わります。`,
      `${seed.signatureMark}に注目すると、珍しい個体を見分けやすくなります。`
    ],
    hiddenRareIds,
    silhouetteKey,
    baseBodyType,
    rarityBias,
    description: `${seed.baseAnimalName}をモチーフにしたWORLDAWNのいきもの。${seed.signatureMark}が特徴です。`
  });

