  "export const getGeneratedIndividualImageCount = (): number => generatedIndividualImageKeys.length;",
  "",
  "export const hasGeneratedIndividualImage = (speciesKey: string, variantKey: IndividualVariantKey): boolean =>",
  "  Boolean(monsterIndividualImages[makeIndividualImageKey(speciesKey, variantKey)]);",
  "",
  "export type IndividualVariantWithImage = IndividualVariant & {",
  "  imageKey: MonsterIndividualImageKey;",
  "  imageSource?: ImageSourcePropType;",
  "};",
  ""
