const ignoredRootDirs = new Set(["old", "backup"]);

const toPosix = (value) => value.split(path.sep).join("/");

const walkPngs = (dir, rootDepth = 0) => {
  if (!fs.existsSync(dir)) {
    return [];
  }

  const files = [];
  for (const entry of fs.readdirSync(dir)) {
    const absolutePath = path.join(dir, entry);
    const stat = fs.statSync(absolutePath);

    if (stat.isDirectory()) {
      if (rootDepth === 0 && ignoredRootDirs.has(entry)) {
        continue;
      }
      files.push(...walkPngs(absolutePath, rootDepth + 1));
      continue;
    }

    if (stat.isFile() && entry.toLowerCase().endsWith(".png")) {
      files.push(absolutePath);
    }
  }

  return files;
};

const getGeneratedFiles = () => {
  const pngsByName = new Map();
  for (const absolutePath of walkPngs(charactersDir)) {
    const fileName = path.basename(absolutePath).toLowerCase();
    if (!pngsByName.has(fileName)) {
      pngsByName.set(fileName, absolutePath);
    }
  }

  const generated = [];
  for (const speciesKey of speciesKeys) {
    for (const variantKey of variantKeys) {
      const imageKey = `${speciesKey}_${variantKey}`;
      const fileName = `${imageKey}.png`;
      const absolutePath = pngsByName.get(fileName.toLowerCase());

      if (absolutePath) {
        generated.push({
          imageKey,
          requirePath: "../../" + toPosix(path.relative(root, absolutePath))
        });
      }
    }
  }

  return generated;
};

const generated = getGeneratedFiles();
